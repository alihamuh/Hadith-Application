package alihamuh.islamic.book.sahihbukhari;

public class ChapterNames {
    private String engNames;
    private String arabicNames;

    public void setArabicNames(String arabicNames) {
        this.arabicNames = arabicNames;
    }

    public void setEngNames(String engNames) {
        this.engNames = engNames;
    }

    public String getArabicNames() {
        return arabicNames;
    }

    public String getEngNames() {
        return engNames;
    }
}
